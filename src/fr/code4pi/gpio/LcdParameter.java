package fr.code4pi.gpio;

/**
 * Static class to give parameters names for LCD management.
 * 
 * @author Benoit Mouquet
 * 
 */
public final class LcdParameter {
	
	/**
	 * Name of parameter for lcd status.
	 */
	public final static String LCD_ON = "lcd_on";

	/**
	 * Name of RS pin parameter in properties file.
	 */
	public final static String LCD_PARAM_PIN_RS = "lcd_rs";

	/**
	 * Name of Strobe pin parameter in properties file.
	 */
	public final static String LCD_PARAM_PIN_STROBE = "lcd_strobe";

	/**
	 * Name of bit 1 pin parameter in properties file.
	 */
	public final static String LCD_PARAM_PIN_BIT_1 = "lcd_bit_1";

	/**
	 * Name of bit 2 pin parameter in properties file.
	 */
	public final static String LCD_PARAM_PIN_BIT_2 = "lcd_bit_2";

	/**
	 * Name of bit 3 pin parameter in properties file.
	 */
	public final static String LCD_PARAM_PIN_BIT_3 = "lcd_bit_3";

	/**
	 * Name of bit 4 pin parameter in properties file.
	 */
	public final static String LCD_PARAM_PIN_BIT_4 = "lcd_bit_4";

	/**
	 * Name of refresh time parameter.
	 */
	public final static String LCD_PARAM_REFRESH_TIME = "lcd_refresh_time";

	/**
	 * Private constructor.
	 */
	private LcdParameter() {

	}

}
